        public EducationFilling() : base("Education", "({0},'{1}','{2}','{3}','{4}','{5}',{6},{7}),") { }

        internal override string GetSql()
        {
            var party = Helper.Party;
            var sb = new StringBuilder(InsertStart);

            for (int i = 0; i < party; i++)
            {
                var educ = GetEduc();
                var lvls = EducationLevel(educ);
                var inst = GetEducatInstit(educ);

                foreach (var lvl in lvls)
                {
                    sb.Append(GetRow(_startId + i,
                                     lvl,
                                     DipSeria(),
                                     DipNumber(),
                                     inst.Item1,
                                     inst.Item2,
                                     YearOfGrad(),
                                     Qualification(educ)));
                }
            }
            sb.Replace(',', ';', sb.Length - 1, 1);
            return sb.ToString();
        }

        /// <summary>
        /// Много SQL-скриптов для исполнения (заполнения таблицы Employee)
        /// </summary>
        internal List<string> GetSqlArr()
        {
            var quot = Helper.RowsCount / Helper.Party;
            var scripts = new List<string>();
            for (int i = 0; i < quot; i++)
            {
                scripts.Add(GetSql());
                _startId += Helper.Party;
            }
            return scripts;
        }

        #region Приватные методы

        private Educ GetEduc()
        {
            var g = _rand.Next(1, 101);
            if (g >= 1 && g <= 2)
                return Educ.School;
            else if (g >= 3 && g <= 18)
                return Educ.College;
            else if (g >= 19 && g <= 70)
                return Educ.Bach;
            else if (g >= 71 && g <= 85)
                return Educ.Mag;
            else
                return Educ.Spec;
        }

        private string[] EducationLevel(Educ educ)
        {
            return educ switch
            {
                Educ.School => new string[] { _educationLvl[0] },
                Educ.College => new string[] { _educationLvl[1] },
                Educ.Bach => new string[] { _educationLvl[2] },
                Educ.Mag => new string[] { _educationLvl[2], _educationLvl[3] },
                Educ.Spec => new string[] { _educationLvl[3] },
                _ => throw new Exception("Что-то пошло не так...")
            };
        }

        private string DipSeria()
        {
            var sb = new StringBuilder();
            for (int i = 0; i < 6; i++)
                sb.Append(Helper.GetDigit());
            return sb.ToString();
        }

        private string DipNumber()
        {
            var sb = new StringBuilder();
            for (int i = 0; i < 7; i++)
                sb.Append(Helper.GetDigit());
            return sb.ToString();
        }

        private (string, string) GetEducatInstit(Educ educ)
        {
            if(educ == Educ.School)
            {
                var i = _rand.Next(0, _schoolLen);
                return _schools[i];
            }
            else if(educ == Educ.College)
            {
                var i = _rand.Next(0, _collegeLen);
                return _colleges[i];
            }
            else
            {
                var i = _rand.Next(0, _univerLen);
                return _universities[i];
            }
        }

        private DateTime YearOfGrad()
        {
            var year = _rand.Next(1950, 2022);
            var month = _rand.Next(1, 13);
            int day = _rand.Next(1, DateTime.DaysInMonth(year, month) + 1);
            return new DateTime(year, month, day);
        }

        private string Qualification(Educ educ)
        {
            return educ switch
            {
                Educ.School => "NULL",
                Educ.College => $"'{_qualificationCollege[_rand.Next(0, _qcLen)]}'",
                Educ.Bach => $"'{_qualificationBach[_rand.Next(0, _qbLen)]}'",
                Educ.Mag => $"'{_qualificationMag[_rand.Next(0, _qmLen)]}'",
                Educ.Spec => $"'{_qualificationSpec[_rand.Next(0, _qsLen)]}'",
                _ => throw new Exception("Что-то пошло не так...")
            };
        }

